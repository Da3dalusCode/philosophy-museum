import type {MuseumAssetRecord, MuseumAssetVariant} from './museumAssetTypes';

type AnalyticAssetInput = Omit<MuseumAssetRecord, 'variants'> & {
  scene: readonly [number, number];
  panel: readonly [number, number];
};

const variant = (
  id: string,
  kind: 'scene' | 'panel',
  size: readonly [number, number],
): MuseumAssetVariant => ({
  path: `assets/museum/analytic-traditions/${id}-${kind}.webp`,
  width: size[0],
  height: size[1],
});

const galleryAsset = ({scene, panel, ...record}: AnalyticAssetInput): MuseumAssetRecord => ({
  ...record,
  variants: {
    scene: variant(record.id, 'scene', scene),
    panel: variant(record.id, 'panel', panel),
  },
});

